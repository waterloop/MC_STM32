	 rawCurrent = getRawCurrent();
	 convertedCurrent = convertCurrent(rawCurrent);

	 //Transmitting both Current and Voltage through UART
	 sprintf(currentMsg, "V: %f C: %f \r\n", convertedVoltage, convertedCurrent);
