/**
  * @brief  The application entry point.
  * @retval int
  */
int main(void)
{
  /* USER CODE BEGIN 1 */
  SVPWM_Init(&svpwm1, 10000);
  VHZ_Init(&vhz1, 0.001, 0.01, 10, 100);

	HAL_StatusTypeDef ret;	//send	temp
	uint8_t buf [12];		// store temp

	  int16_t val;
	  float temp_c;

	uint16_t raw;	// store voltage
	char msg[10];


  /* USER CODE END 1 */

  /* MCU Configuration--------------------------------------------------------*/

  /* Reset of all peripherals, Initializes the Flash interface and the Systick. */
  HAL_Init();

  /* USER CODE BEGIN Init */

  /* USER CODE END Init */

  /* Configure the system clock */
  SystemClock_Config();

  /* USER CODE BEGIN SysInit */

  /* USER CODE END SysInit */

  /* Initialize all configured peripherals */
  MX_GPIO_Init();
  MX_TIM1_Init();
  MX_TIM15_Init();
  MX_USART2_UART_Init();
  MX_I2C1_Init();
  MX_ADC1_Init();
  /* USER CODE BEGIN 2 */
  HAL_TIM_PWM_Start(&htim1, TIM_CHANNEL_1);
  HAL_TIMEx_PWMN_Start(&htim1, TIM_CHANNEL_1);
  HAL_TIM_PWM_Start(&htim1, TIM_CHANNEL_2);
  HAL_TIMEx_PWMN_Start(&htim1, TIM_CHANNEL_2);
  HAL_TIM_PWM_Start(&htim1, TIM_CHANNEL_3);
  HAL_TIMEx_PWMN_Start(&htim1, TIM_CHANNEL_3);
  HAL_TIM_Base_Start_IT(&htim15);

  // Chip select pin should default high
  HAL_GPIO_WritePin(GPIOB, GPIO_PIN_2, GPIO_PIN_SET);
  /* USER CODE END 2 */

  /* Infinite loop */
  /* USER CODE BEGIN WHILE */
  while (1)
  {
	  HAL_ADC_Start(&hadc1);
	  HAL_ADC_PollForConversion(&hadc1, HAL_MAX_DELAY);
	  raw = HAL_ADC_GetValue(&hadc1);

		buf [0] = REG_TEMP;//REQUEST TO READ
			  ret = HAL_I2C_Master_Transmit(&hi2c1, TMP102_ADDR, buf, 1, HAL_MAX_DELAY);
			  if (ret != HAL_OK)
			  {
				  	 strcpy ((char*)buf, "Error Tx\r\n");
			  }
			  else
			  {
				  //READ 2 bytes from the temperature register
			      ret = HAL_I2C_Master_Receive(&hi2c1, TMP102_ADDR, buf, 2, HAL_MAX_DELAY);
				  if (ret != HAL_OK)
				  {
					  	 strcpy ((char*)buf, "Error Tx\r\n");
				  }
				  else
				  {
					  //combine the bytes
					  val = ((int16_t)buf[0]<<4) | (buf[1] >> 4);
					  //convert to 2's complement, since temperature can be negative
					  if  (val > 0x7FF)
					  {
						  val |= 0xF000;
					  }
					  // Convert to float temperature value (Celsius)
					  temp_c = val * 0.0625;
					  // Convert temperature to decimal format
					  temp_c *= 100;
					  sprintf ((char*)buf,
				              "%u.%u C\r\n",
							  ((unsigned int)temp_c/100),
							  ((unsigned int)temp_c %100));
				  }
			  }
		// Send out buffer (temperature or error message)
		HAL_UART_Transmit(&huart2, buf, strlen((char*)buf), HAL_MAX_DELAY);

		sprintf(msg, "%hu\r\n", raw);
		HAL_UART_Transmit(&huart2, (uint8_t*)msg, strlen(msg), HAL_MAX_DELAY);

    /* USER CODE END WHILE */

    /* USER CODE BEGIN 3 */
  }
  /* USER CODE END 3 */
}

